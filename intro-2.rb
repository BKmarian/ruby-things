# Truthy and falsey in Ruby

if true
  puts "true is truthy, duh!"
else
  puts "true is falsey, wtf!"
end

if false
  puts "false is truthy, wtf!"
else
  puts "false is falsey, duh!"
end

if nil
  puts "nil is truthy"
else
  puts "nil is falsey"
end

if 0
  puts "0 is truthy"
else
  puts "0 is falsey"
end

